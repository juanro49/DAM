    private void calcularPasajeros ()
    {
        
        Enumeration e = convoy.contenedor.elements();
        
        while (e.hasMoreElements())
        {
            
            Barco b = (Barco) e.nextElement();
            pasajeros += b.getNum_pasajeros();
            
        }
        
        txtPasajeros.setText(String.valueOf(pasajeros));
        
    }
