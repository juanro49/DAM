    private void btnSeguirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeguirActionPerformed
        try
        {
            exp = Integer.parseInt(txtExp.getText());
        
            if(miArchivo.existeAlumno(fichero, exp))
            {
                JOptionPane.showMessageDialog(rootPane, "El usuario ya existe");
            }
            else
            {
                jPanel2.setVisible(true);
            }
        }
        catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(rootPane, "Formato de expediente incorrecto");
        }
    }//GEN-LAST:event_btnSeguirActionPerformed

