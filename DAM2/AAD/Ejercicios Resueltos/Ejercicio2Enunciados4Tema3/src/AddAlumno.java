    private void btnElegirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnElegirActionPerformed
        int resp = fcSelecFichero.showOpenDialog(this);

        if (resp == JFileChooser.APPROVE_OPTION)
        {
            JOptionPane.showMessageDialog(null, fcSelecFichero.getSelectedFile().toString());
            fichero = fcSelecFichero.getSelectedFile().toString();
            jPanel3.setVisible(true);
            jPanel1.setVisible(false);
        }
        else if (resp==JFileChooser.CANCEL_OPTION)
        {

            JOptionPane.showMessageDialog(null,"Operaci칩n cancelada");
        }
    }//GEN-LAST:event_btnElegirActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String cad = "", nom;
        int notas[] = new int[3];
        boolean b = true;

        if(txtNom.getText().isEmpty())
        {
            cad += "Introduce un nombre\n";
            b = false;
        }

        if(txtN1.getText().isEmpty())
        {
            cad += "Introduce la nota 1\n";
            b = false;
        }

        if(txtN2.getText().isEmpty())
        {
            cad += "Introduce la nota 2\n";
            b = false;
        }

        if(txtN3.getText().isEmpty())
        {
            cad += "Introduce la nota 3\n";
            b = false;
        }

        if(b)
        {
            try
            {
                nom = txtNom.getText();
                notas[0] = Integer.parseInt(txtN1.getText());
                notas[1] = Integer.parseInt(txtN2.getText());
                notas[2] = Integer.parseInt(txtN3.getText());

                miArchivo.a침adir(fichero, exp, nom, notas);
            }
            catch(NumberFormatException e)
            {
                JOptionPane.showMessageDialog(rootPane, "Formato de saldo incorrecto");
            }
            catch (IOException e)
            {
                JOptionPane.showMessageDialog(rootPane, e.getMessage());
            }
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane, cad);
        }
    }//GEN-LAST:event_btnAddActionPerformed

