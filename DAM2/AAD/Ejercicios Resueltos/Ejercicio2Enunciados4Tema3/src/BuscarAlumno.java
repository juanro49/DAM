    private void btnElegirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnElegirActionPerformed
        int resp = fcSelecFichero.showOpenDialog(this);

        if (resp == JFileChooser.APPROVE_OPTION)
        {
            JOptionPane.showMessageDialog(null, fcSelecFichero.getSelectedFile().toString());
            fichero = fcSelecFichero.getSelectedFile().toString();
            jPanel3.setVisible(true);
            jPanel1.setVisible(false);
        }
        else if (resp==JFileChooser.CANCEL_OPTION)
        {

            JOptionPane.showMessageDialog(null,"Operación cancelada");
        }
    }//GEN-LAST:event_btnElegirActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try
        {
            exp = Integer.parseInt(txtExp.getText());
            Alumno alumno = miArchivo.buscarAlumno(fichero, exp);
            
            if(alumno != null)
            {
                txtNom.setText(alumno.getNombre());
                txtN1.setText(String.valueOf(alumno.getNotas()[0]));
                txtN2.setText(String.valueOf(alumno.getNotas()[1]));
                txtN3.setText(String.valueOf(alumno.getNotas()[2]));
                jPanel2.setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane, "El usuario no existe");
            }
        }
        catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(rootPane, "Formato de expediente incorrecto");
        }
        catch (IOException ex)
        {
            Logger.getLogger(BuscarAlumno.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

